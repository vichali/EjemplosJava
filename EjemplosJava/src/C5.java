public class C5 {

	/**
	 * Ciclo Iterativo while
	 */

	public static void main(String[] args)  {

        int i = 0;
        while (i < 8) {
            System.out.print(++i);
            System.out.print("\t");
        }
            
		System.out.println();
		System.out.println("termino el while");
		
	}
}



