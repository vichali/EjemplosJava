public class C6 {

	/**
	 * Ciclo Iterativo do while
	 */

	public static void main(String[] args)  {

        int i = 0;
        
        do {
            System.out.print(++i);
            System.out.print("\t");
        } while (i < 8); // fin del while
            
		System.out.println();
		System.out.println("termino el do while");
		
		
	} 
}



